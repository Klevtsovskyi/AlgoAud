# 10 -> 1010_2
# 12 -> 1100_2

# &: 10 & 12 -> 1000_2 -> 8
# |: 10 | 12 -> 1110_2 -> 14
# ^: 10 ^ 12 -> 0110_2 -> 6
# ~: ~10 -> -11
# <<: 10 << 1 -> 10100 -> 20
# >>: 10 >> 1 -> 101 -> 5


def solve(n):
    count = 0
    while n > 0:
        if n & 1:
            count += 1
        n = n >> 1
    return count


if __name__ == "__main__":
    n = int(input())
    print(solve(n))
