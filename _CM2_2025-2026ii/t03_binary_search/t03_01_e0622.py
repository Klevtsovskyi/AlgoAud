# 12 -> 1100
# 10 -> 1010

# 12 & 10 -> 1000 -> 8
# 12 | 10 -> 1110 -> 14
# 12 ^ 10 -> 0110 -> 6
# ~12 -> 11110011

# -1 -> 11111111
# -2 -> 11111110
# 127 -> 01111111
# -128 -> 10000000

# 12 >> 1 -> 110 -> 6
# 12 << 1 -> 11000 -> 24


def solve(n: int) -> int:
    count = 0
    while n > 0:
        count += n & 1
        n = n >> 1
    return count


if __name__ == '__main__':
    n = int(input())
    print(solve(n))
